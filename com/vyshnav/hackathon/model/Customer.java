package com.vyshnav.hackathon.model;

public class Customer {

	private String name;
	private int cashInHand;

	public String getName() {
		return name;
	}

	public void setName() {
		this.name = name;
	}
	
	public int getCashInHand() {
		return cashInHand;
	}
	
	public void setCashInHand(int cashInHand) {
		this.cashInHand = cashInHand;
	}

}
